function color = mult2color(multip)
if multip==0
    color = 'Black';
elseif multip==1
    color = 'Brown';
elseif multip==2
    color = 'Red';
elseif multip==3
    color = 'Orange';
elseif multip==4
    color = 'Yellow';
elseif multip==5
    color = 'Green';
elseif multip==6
    color = 'Blue';
elseif multip==7
    color = 'Violet';
elseif multip==8
    color = 'Grey';
elseif multip==9
    color = 'White';
end
end